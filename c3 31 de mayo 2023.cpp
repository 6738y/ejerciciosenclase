#include<stdio.h>
int main ()
{
	int m=45,n=75;
	++m;
	--n;
	printf("m=%d, n=%d, \n",m,n);
	m++;
	n--;
	printf("m=%d, n=%d\n",m,n);
	m++;
	n--;
	printf("m=%d, n=%d, \n",m,n);
}
